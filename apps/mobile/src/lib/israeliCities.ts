export function matchCities(query: string, limit = 7): string[] {
  const q = query.trim();
  if (!q) return [];
  return IL_CITIES.filter(
    (c) => c.includes(q) || c.toLowerCase().includes(q.toLowerCase()),
  ).slice(0, limit);
}
